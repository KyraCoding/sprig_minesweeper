function initializeLevel(level) {
  clearText()
  switch (level) {
    case 0:
      setMap(levels[0])
      addText("MINESWEEPER", { 
        x: 4,
        y: 2,
        color: color`0`
      })
      addText("PLAY", { 
        x: 8,
        y: 5,
        color: color`0`
      })
      addText("SETTINGS", { 
        x: 6,
        y: 9,
        color: color`0`
      })
      addText("CREDITS", { 
        x: 6,
        y: 13,
        color: color`0`
      })
      onInput("w", () => {
        addSprite(1,1,boxLeftSelected)
        addSprite(2,1,boxCenterSelected)
        addSprite(3,1,boxRightSelected)
        
      })

  }
}
